public class J4datatype {
    public static void main(String[] args) {
        byte b=8;
        System.out.println(b);
        char ch='a';
        System.out.println(ch);
        boolean var=true;
        float price=10.5f;
        int num=25;
        short n=240;
        System.out.println(price);
        System.out.println(n);
        System.out.println(num);
        System.out.println(var);
    }
}
