public class Switch {
    public static void main(String[] args) {
        int n=2;
        switch (n) {
            case 1:
                System.out.println("this is 1");
                break;
            case 2:
                System.out.println("this is 2");
                break;
        
            default:
            System.out.println("wrong");
                break;
        }
       }
}
